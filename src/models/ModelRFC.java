public class ModelRFC {
    private String nombre;
    private String apep;
    private String apem;
    private String dia_n;
    private String mes_n;
    private String anio_n;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getApep() {
        return apep;
    }

    public void setApep(String apep) {
        this.apep = apep;
    }

    public String getApem() {
        return apem;
    }

    public void setApem(String apem) {
        this.apem = apem;
    }

    public String getDia_n() {
        return dia_n;
    }

    public void setDia_n(String dia_n) {
        this.dia_n = dia_n;
    }

    public String getMes_n() {
        return mes_n;
    }

    public void setMes_n(String mes_n) {
        this.mes_n = mes_n;
    }

    public String getAnio_n() {
        return anio_n;
    }

    public void setAnio_n(String anio_n) {
        this.anio_n = anio_n;
    }
}
